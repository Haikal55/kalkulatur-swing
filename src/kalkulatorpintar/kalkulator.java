    private void btnPlusMinusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlusMinusActionPerformed
        // TODO add your handling code here:
        if (expression.isEmpty()) return;

        int i = expression.length() - 1;
        while (i >= 0 && (Character.isDigit(expression.charAt(i)) || expression.charAt(i) == '.')) {
            i--;
        }

        String before = expression.substring(0, i + 1);
        String num = expression.substring(i + 1);

        if (num.startsWith("-")) {
            num = num.substring(1);
        } else {
            num = "-" + num;
        }

        expression = before + num;
        jTextField1.setText(expression);
    }//GEN-LAST:event_btnPlusMinusActionPerformed

