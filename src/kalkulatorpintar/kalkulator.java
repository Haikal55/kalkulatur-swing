    private void calculate() {
        try {
            if (expression.isEmpty()) return;

            String exp = expression;
            exp = exp.replaceAll("\\+\\+", "+");
            exp = exp.replaceAll("--", "+");
            exp = exp.replaceAll("\\+-", "-");
            exp = exp.replaceAll("-\\+", "-");

            java.util.List<String> tokens = new java.util.ArrayList<>();
            String number = "";

            for (int i = 0; i < exp.length(); i++) {
                char c = exp.charAt(i);

                if (Character.isDigit(c) || c == '.' || c == '%' ||
                    (c == '-' && (i == 0 || "+-*/".contains("" + exp.charAt(i - 1))))) {
                    number += c;
                } else if ("+-*/".indexOf(c) >= 0) {
                    tokens.add(number);
                    tokens.add(String.valueOf(c));
                    number = "";
                }
            }
            tokens.add(number);

            double result = parseNumber(tokens.get(0));

            for (int i = 1; i < tokens.size(); i += 2) {
                String op = tokens.get(i);
                double next = parseNumber(tokens.get(i + 1));
                switch (op) {
                    case "+": result += next; break;
                    case "-": result -= next; break;
                    case "*": result *= next; break;
                    case "/": result /= next; break;
                }
            }

            expression = String.valueOf(result);
            jTextField1.setText(expression);

        } catch (Exception e) {
            expression = "";
            jTextField1.setText("Error");
        }
    }
    
    private double parseNumber(String token) {
        if (token.endsWith("%")) {
            double val = Double.parseDouble(token.substring(0, token.length() - 1));
            return val / 100;
        }
        return Double.parseDouble(token);
    }



